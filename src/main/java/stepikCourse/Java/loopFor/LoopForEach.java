package stepikCourse.Java.loopFor;

public class LoopForEach {
    public static void main(String[] args) {

        /**
         * увеличиваем цикл на 2 brand получаем нечетное число
         */
        for (int j = 1; j<11;j=j+2) { // j<2 это булевое значение если
            System.out.println(j);
        }

        /**
         * стандартный цикл
         */
        for(int i = 1; i<=2; i++) {
            String m = "dfddff";
            System.out.println(m);
        }
    }
}
